# Lista é uma coleção ordenado e mutavel, Permite membros duplicados
lista = ['a', 1, 1.5, False]


# Tupla é uma coleção ordenado e imutavel, Permite membros duplicados
tupla = ('a', 1, 1.5, False)


# Dicionario é uma coleção ordenado e mutavel, nao permite membro duplicado, (podemos atribuir valor a algo) (para acessa utilizamos a chave como indice)
dicionario = {'carro':2,'dinheiro':2.5, 'Are you ok?': True, 'string': 'string'}
print(dicionario['Are you ok?'])


# Set é uma coleção não ordenada e nao indexada (nao possui indice), nenhnum membro duplicado
set = {1, 2.5, 'a', True}